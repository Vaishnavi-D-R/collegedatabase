package com.mindtree.collegedatabase.exception;

public class CollegeException  extends Exception {

	public CollegeException() {
	}

	public CollegeException(String arg0) {
		super(arg0);
	}

	public CollegeException(Throwable arg0) {
		super(arg0);
	}

	public CollegeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CollegeException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}


}
